# Tupples are list that can't be updated later
# Tupple packing is creating tupple without ()
# Single element tupple needs trailing comma
# Tupple can be reassigned

# Creating
print("creating tupple")
tpl = (1, 2, 3)
print(tpl)

print("creating tupple without () ")
tpl = 1, 2, 3
print(tpl)

print("creating single element tupple ")
tpl = (1,)
print(tpl)

# rest of all operations are same as list except update
